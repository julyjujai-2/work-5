income = float(input("กรุณากรอกจำนวนรายได้ของคุณ (บาท): "))
if income < 15000:
    print(
        "ขออภัย, คุณไม่สามารถทำบัตรเครดิตได้เนื่องจากรายได้ต่ำกว่า 15000 บาท")
elif income < 70000:
    print("คุณสามารถทำบัตรเครดิตประเภท 'บัตรเงิน' ได้")
elif income < 100000:
    print("คุณสามารถทำบัตรเครดิตประเภท 'บัตรทอง' ได้")
else:
    print("คุณสามารถทำบัตรเครดิตประเภท 'บัตร Platinum' ได้")
